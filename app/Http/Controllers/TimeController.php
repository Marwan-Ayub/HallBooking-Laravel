     */
    public function update(Request $request, $id)
    {
        $validated = $request->validate([
            'timeStart' => ['required'],
            'timeEnd' => ['required'],
        ]);

        $time = time::findOrFail($id);
        $time->update($validated);

        return redirect()->route('admin.times.index')->with('message', 'Time updated successfully');
